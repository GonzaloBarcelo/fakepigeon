
function initialMessageLoad() {
    try {
        const data = {userLoggedIn: document.getElementById("userLoggedIn").value};
        const address = '/initialMessageLoad';

        fetch(address, {
            method: 'POST',
            headers: {
                'Content-Type': 'application/json'
            },
            body: JSON.stringify(data)
        })
        .then(response => response.json())
        .then(data => {
            console.log(data);
        });

    } catch (err) {
        console.error(err.message);
    }
    return false;
}

